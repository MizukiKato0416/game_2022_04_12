//=============================================================================
// デフォルトコンストラクタ
//=============================================================================
CHappenig::CHappenig(CObject::PRIORITY Priority) : CObject(Priority)
{
	m_pos = D3DXVECTOR3(0.0f, 0.0f, 0.0f);
	m_rot = D3DXVECTOR3(0.0f, 0.0f, 0.0f);
	m_model = nullptr;
}

