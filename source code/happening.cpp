// デフォルトデストラクタ
//=============================================================================
CHappenig::~CHappenig()
{

}

//=============================================================================
// 初期化
//=============================================================================
HRESULT CHappenig::Init(void)
{
	CObject::Init();

	SetObjType(CObject::OBJTYPE::HAPPENING);

	return S_OK;
}

//=============================================================================
// 終了
//=============================================================================
void CHappenig::Uninit(void)
{
	if (m_model != nullptr)
	{
		m_model->Uninit();
		m_model = nullptr;
	}
	Release();
}

